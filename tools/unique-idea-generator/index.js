var type = 2;

function checkSettings() {

    if (document.getElementById("themes").checked == true && document.getElementById("gimmicks").checked == true) {
        type = 2;
        printIdea();
    } else if (document.getElementById("themes").checked == true && document.getElementById("gimmicks").checked == false) {
        type = 1
        printIdea();
    } else if (document.getElementById("themes").checked == false && document.getElementById("gimmicks").checked == true) {
        type = 0
        printIdea();
    } else {
        type = -1;
        printIdea();
    }

}

